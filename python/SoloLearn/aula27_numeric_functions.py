# Para encontrar o máximo ou o mínimo de alguns números ou uma lista, você pode usar max ou min .
print(max([1, 4, 9, 2, 5, 6, 8]))

print(min(1, 2, 3, 4, 0, 2, 1))

# Para encontrar a distância de um número de zero (seu valor absoluto), use abs .
print(abs(-99))
print(abs(42))

# Para arredondar um número para um determinado número de casas decimais, use arredondar .
print(round(2.8))


# Para encontrar o total de uma lista, use sum .
print(sum([1, 2, 3, 4, 5]))